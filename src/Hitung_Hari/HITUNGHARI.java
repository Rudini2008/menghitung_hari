    private void hitungButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungButtomActionPerformed
         if(tahunTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Error : isian tahun kososng!",
                    "Error:isian tahun kosong!",JOptionPane.WARNING_MESSAGE);
        }else{
            int tahun = Integer.parseInt(tahunTextField.getText());
            int jumlahHari;
            if(bulanComBox.getSelectedItem().equals("Februari")){
                if (((tahun % 4== 0)&&!(tahun % 100 == 0))||(tahun % 400 == 0))
                    jumlahHari=29;
                else jumlahHari=28;
            }else if(bulanComBox.getSelectedItem().equals("April")||
                     bulanComBox.getSelectedItem().equals("Juni")||
                     bulanComBox.getSelectedItem().equals("September")||
                    bulanComBox.getSelectedItem().equals("November")){
                     jumlahHari=30;
        }else{
    jumlahHari=31;
        }
         lbjumlah.setText("Jumlah Hari Pada Bulan "+
                bulanComBox.getSelectedItem()+
                 " Tahun "+tahun+" adalah "+jumlahHari);
         }
        
    }//GEN-LAST:event_hitungButtomActionPerformed

